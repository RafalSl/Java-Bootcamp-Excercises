package dodawanie;

public class Dodawanie {
	public static int dodaj(int a, int b) {
		System.out.print("Dodawanie int: ");
		return a+b;
	}
	
	public static float dodaj(float a, float b) {
		System.out.print("Dodawanie float: ");
		return a+b;
	}
	
	public static double dodaj(double a, double b) {
		System.out.print("Dodawanie double: ");
		return a+b;
	}
}
